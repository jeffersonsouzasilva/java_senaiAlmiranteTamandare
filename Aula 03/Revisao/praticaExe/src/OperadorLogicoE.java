public class OperadorLogicoE {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 10;

        if (num1 > 0 && num2 > 0) {
            System.out.println("Ambos os números são positivos.");
        } else {
            System.out.println("Um ou ambos os números não são positivos.");
        }
    }
}
