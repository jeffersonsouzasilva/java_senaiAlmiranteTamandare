    //m√©todo para consultar o faturamento 
    private void consultar_faturamento() {
        String sql = "select substr(data_os,1,7) as Data, sum(valor) as Faturamento_Mensal from tbos group by data";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();

            DefaultTableModel modelo = (DefaultTableModel) tblRelatorio.getModel();
            modelo.setRowCount(0);

            ResultSetMetaData metaData = rs.getMetaData();
            int colunas = metaData.getColumnCount();
            modelo.setColumnCount(0);
            for (int i = 1; i <= colunas; i++) {
                modelo.addColumn(metaData.getColumnName(i));
            }
            while (rs.next()) {
                Object[] linha = new Object[colunas];
                for (int i = 1; i <= colunas; i++) {
                    linha[i - 1] = rs.getObject(i);
                }
                modelo.addRow(linha);
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

